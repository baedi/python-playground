"""
    출력(print) 고급 예시 코드
"""

# 문자 이어쓰기 예제 (붙이기)
print("You" "Need" "More" "Practice")
print("You" + "Need" + "More" + "Practice")

# 문자 이어쓰기 예제 (띄어쓰기)
print("You", "need", "more", "practice!")
print("")

# end 인자를 추가하면 줄바꿈 대신 다른 문자로 끝낼 수 있다.
print("Hello", end=" ")
print("it's nice to see you.")
print("How's it going?")
